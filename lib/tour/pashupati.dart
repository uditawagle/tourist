class Pashupatinath extends StatefulWidget {
  const Pashupatinath({Key? key}) : super(key: key);

  @override
  State<Pashupatinath> createState() => _PashupatinathState();
}

class _PashupatinathState extends State<Pashupatinath> {
  late PageController _pageController;

  void _onScroll() {}

  @override
  void initState() {
    _pageController = PageController(
      initialPage: 0,
    )..addListener(() {
        _onScroll;
      });

    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: PageView(
        controller: _pageController,
        children: [
          Details( 
            image: 'assets/travel/temple.jpg',
          ),
          PeopleDetails(image:'assets/people/pashupatipeople.jpg'),
        ],
      ),
    );
  }
}

