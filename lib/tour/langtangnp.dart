class  LangtangNP extends StatefulWidget {
  const LangtangNP ({Key? key}) : super(key: key);

  @override
  State<LangtangNP> createState() => _LangtangNPState();
}

class _LangtangNPState extends State<LangtangNP > {
  late PageController _pageController;

  void _onScroll() {}

  @override
  void initState() {
    _pageController = PageController(
      initialPage: 0,
    )..addListener(() {
        _onScroll;
      });

    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: PageView(
        controller: _pageController,
        children: [
          Details( 
            image: 'assets/travel/langtangtrek.jpg',
          ),
          PeopleDetails(image:'assets/travel/langtang.jpg'),
        ],
      ),
    );
  }
}

