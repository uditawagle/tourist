  @override
  State<TrishuliRiver> createState() => _TrishuliRiverState();
}

class _TrishuliRiverState extends State<TrishuliRiver> {
  late PageController _pageController;

  void _onScroll() {}

  @override
  void initState() {
    _pageController = PageController(
      initialPage: 0,
    )..addListener(() {
        _onScroll;
      });

    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: PageView(
        controller: _pageController,
        children: [
          Details(
            image: 'assets/people/river.jpg',
          ),
          PeopleDetails(image:'assets/people/trisulipeople.jpg'),
        ],
      ),
    );
  }
}

