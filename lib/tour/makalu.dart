class Makalu extends StatefulWidget {
  const Makalu({Key? key}) : super(key: key);

  @override
  State<Makalu> createState() => _MakaluState();
}

class _MakaluState extends State<Makalu> {
  late PageController _pageController;

  void _onScroll() {}

  @override
  void initState() {
    _pageController = PageController(
      initialPage: 0,
    )..addListener(() {
        _onScroll;
      });

    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: PageView(
        controller: _pageController,
        children: [
          Details(
            image: 'assets/travel/makalu.jpg',
          ),
          PeopleDetails(image:'assets/people/camppeople.jpg'),
        ],
      ),
    );
  }
}

