  @override
  State<Dakshinkali> createState() => _DakshinkaliState();
}

class _DakshinkaliState extends State<Dakshinkali > {
  late PageController _pageController;

  void _onScroll() {}

  @override
  void initState() {
    _pageController = PageController(
      initialPage: 0,
    )..addListener(() {
        _onScroll;
      });

    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: PageView(
        controller: _pageController,
        children: [
          Details( 
            image: 'assets/people/dak.jpg',
          ),
          PeopleDetails(image:'assets/people/dakpeople.jpg'),
        ],
      ),
    );
  }
}

