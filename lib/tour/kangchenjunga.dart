class  Kangchenjunga extends StatefulWidget {
  const Kangchenjunga ({Key? key}) : super(key: key);

  @override
  State<Kangchenjunga> createState() => _KangchenjungaState();
}

class _KangchenjungaState extends State<Kangchenjunga > {
  late PageController _pageController;

  void _onScroll() {}

  @override
  void initState() {
    _pageController = PageController(
      initialPage: 0,
    )..addListener(() {
        _onScroll;
      });

    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: PageView(
        controller: _pageController,
        children: [
          Details( 
            image: 'assets/travel/kanchenjunga.jpg',
          ),
          PeopleDetails(image:'assets/travel/.jpg'),
        ],
      ),
    );
  }
}

