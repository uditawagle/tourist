class ChanguTemple extends StatefulWidget {
  const ChanguTemple({Key? key}) : super(key: key);

  @override
  State<ChanguTemple> createState() => _ChanguTempleState();
}

class _ChanguTempleState extends State<ChanguTemple> {
  late PageController _pageController;

  void _onScroll() {}

  @override
  void initState() {
    _pageController = PageController(
      initialPage: 0,
    )..addListener(() {
        _onScroll;
      });

    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: PageView(
        controller: _pageController,
        children: [
          Details( 
            image: 'assets/travel/changu.jpg',
          ),
          PeopleDetails(image:'assets/people/bhaktapeople.jpg'),
        ],
      ),
    );
  }
}

