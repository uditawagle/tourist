  @override
  State<Budhanilkantha> createState() => _BudhanilkanthaState();
}

class _BudhanilkanthaState extends State<Budhanilkantha > {
  late PageController _pageController;

  void _onScroll() {}

  @override
  void initState() {
    _pageController = PageController(
      initialPage: 0,
    )..addListener(() {
        _onScroll;
      });

    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: PageView(
        controller: _pageController,
        children: [
          Details( 
            image: 'assets/people/budpeople.jpg',
          ),
          PeopleDetails(image:'assets/travel/budhanilkantha.jpg'),
        ],
      ),
    );
  }
}

