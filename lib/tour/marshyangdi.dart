class _MarshyangdiRiverState extends State<MarshyangdiRiver> {
  late PageController _pageController;

  void _onScroll() {}

  @override
  void initState() {
    _pageController = PageController(
      initialPage: 0,
    )..addListener(() {
        _onScroll;
      });

    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: PageView(
        controller: _pageController,
        children: [
          Details(
            image: 'assets/travel/marshyangdi.jpg',
          ),
          PeopleDetails(image:'assets/people/setiriver.jpg'),
        ],
      ),
    );
  }
}

